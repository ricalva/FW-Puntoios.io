  <p>Algunos tipos de programacion web:</p>

<p>

<p>Frontend</p>

La parte de un sitio web con la que el usuario interactúa directamente se denomina interfaz. También se conoce como el "lado del cliente" de la aplicación. Incluye todo lo que los usuarios experimentan directamente: colores y estilos de texto, imágenes, gráficos y tablas, botones, colores y menú de navegación. HTML, 
CSS y JavaScript son los lenguajes utilizados para el desarrollo de Front End. Los desarrolladores front-end implementan la estructura, el diseño, el comportamiento y el contenido de todo lo que se ve en las pantallas del navegador cuando se abren sitios web, aplicaciones web o aplicaciones móviles.

<p>Backend</p>

El backend es el lado del servidor del sitio web. Almacena y organiza los datos, y también se asegura de que todo en el lado del cliente del sitio web funcione correctamente. Es la parte del sitio web que no puede ver ni con la que no puede interactuar. Es la parte del software que no entra en contacto
 directo con los usuarios. Los usuarios acceden indirectamente a las partes y características desarrolladas por los diseñadores de back-end a través de una aplicación de front-end. Las actividades, como escribir API, crear bibliotecas y trabajar con componentes del sistema sin interfaces de usuario o incluso
  sistemas de programación científica, también se incluyen en el backend. 

